package ASCII_Table; 

public class PosOrNeg{
    public static void main(String[] args){
        int num=5;

        if(num > 0){
            System.out.println("The number is positive");
        }else if(num<0){
            System.out.println("The number is Negative");
        }else{
            System.out.println("Zero");
        }

    }
}